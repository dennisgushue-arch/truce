function withGoalScripts(pack) {
  return {
    ...pack,
    goalScripts: {
      repair: {
        soft: pack.repair?.[0] ?? pack.scripts.soft,
        direct: pack.scripts.soft,
        firm: pack.repair?.[1] ?? pack.scripts.direct,
      },
      boundary: {
        soft: pack.textShield.boundary ?? pack.scripts.direct,
        direct: pack.scripts.firm,
        firm: pack.scripts.firm,
      },
      problemSolve: {
        soft: pack.scripts.soft,
        direct: pack.scripts.direct,
        firm: pack.nextMoves?.[0] ?? pack.scripts.direct,
      },
    },
  }
}

